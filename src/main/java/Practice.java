public class Practice {
    public static void main(String[] args) {
        int num1, num2, result;
        num1 = 1050;
        num2 = 168;

        result = num1 + num2;
        System.out.println(num1 + "+" + num2 + "=" + result);

        result = num1 - num2;
        System.out.println(num1 + "-" + num2 + "-" + result);

        result = num1 * num2;
        System.out.println(num1 + "x" + num2 + "=" + result);

        result = num1 / num2;
        System.out.println(num1 + "/" + num2 + "=" + result);
    }
}
